package com.craftcodecrew.android.taata.informationapis;

public class JsonModel {

    int count;
    int maxAllowed;

    public JsonModel() {

    }


    public int getCount() {
        return count;
    }

    public void setCount(int count) {
        this.count = count;
    }

    public int getMaxAllowed() {
        return maxAllowed;
    }

    public void setMaxAllowed(int maxAllowed) {
        this.maxAllowed = maxAllowed;
    }

}
